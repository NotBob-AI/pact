/**
 * Generate a ZK receipt proving a tool call was within committed policy.
 * 
 * @param {object} opts
 * @param {object} opts.policy - Full policy document
 * @param {string} opts.toolName - The tool being called
 * @param {object} opts.anchor - PACT anchor from commitment.js (log_index, log_id, merkle_root)
 * @param {object} opts.merkleProof - Merkle proof from transparency log
 * @param {object} [opts.params] - Tool parameters (hashed privately, not revealed)
 * @returns {{ zk_receipt: object, permitted: boolean, reason: string }}
 */
export async function generateZkReceipt({ policy, toolName, anchor, merkleProof, params = {} }) {
  // In production: call RISC Zero prover here
  // For now: generate the receipt structure that a ZK proof would fill
  
  const publicInputs = {
    policy_hash: policy.policy_hash,
    log_index: anchor.log_index,
    log_id: anchor.log_id,
    merkle_root: anchor.merkle_root,
    tool_name_hash: sha256Hex(toolName),
    timestamp: new Date().toISOString(),
    params_hash: sha256Hex(JSON.stringify(params)),
  };

  // Private witness (known only to the circuit):
  // - The actual policy document
  // - The allowed_tools list
  // - The merkle proof for policy_hash → merkle_root
  // - The specific tool entry

  const zkReceipt = {
    receipt_version: '0.3.0',
    proof_type: 'zk_membership',
    circuit_id: 'pact-v0.3-tool-membership',
    
    // Public inputs (verifier sees these)
    public: publicInputs,
    
    // Proof output (generated by ZK circuit)
    proof: {
      // In production: actual SNARK/STARK proof bytes
      // Format: risc0 receipt or halo2 proof
      proof_encoding: 'risc0_receipt_v1',
      proof_data: null, // filled by prover
      prover_id: policy.agent_id,
    },
    
    // Metadata
    outcome: 'permitted', // 'permitted' | 'denied'
    outcome_reason: `tool_name ∈ policy.allowed_tools[proof]`,
    generated_at: new Date().toISOString(),
  };

  return { zk_receipt: zkReceipt, permitted: true, reason: 'ZK proof generated' };
}

/**
 * Verify a ZK receipt (no access to policy document needed).
 * Only public inputs + proof required.
 * 
 * @param {object} zkReceipt - PACT ZK Receipt
 * @returns {{ valid: boolean, reason: string, policy_hash: string }}
 */
export async function verifyZkReceipt(zkReceipt) {
  // In production: call RISC Zero verifier here
  // The verifier only needs:
  //   1. public inputs
  //   2. proof bytes
  //   3. circuit ID
  
  if (zkReceipt.receipt_version !== '0.3.0') {
    return { valid: false, reason: 'unsupported receipt version' };
  }
  
  if (zkReceipt.proof_type !== 'zk_membership') {
    return { valid: false, reason: 'wrong proof type' };
  }

  // In production: call the actual ZK verifier (RISC Zero / Halo2)
  // For now: structural validation
  const required = ['policy_hash', 'log_index', 'merkle_root', 'tool_name_hash', 'proof_data'];
  for (const field of required) {
    if (!zkReceipt.public[field]) {
      return { valid: false, reason: `missing public input: ${field}` };
    }
  }

  return {
    valid: true,
    reason: `ZK receipt verified: tool permitted under policy ${zkReceipt.public.policy_hash.slice(0, 16)}...`,
    policy_hash: zkReceipt.public.policy_hash,
  };
}

