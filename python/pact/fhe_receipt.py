def fhe_receipt_to_dict(receipt: FHEReceipt) -> dict:
    """Serialize FHEReceipt to JSON dict."""
    return {
        "version": receipt.version,
        "receipt_id": receipt.receipt_id,
        "issued_at": receipt.issued_at,
        "envelope_id": receipt.envelope_id,
        "agent_id": receipt.agent_id,
        "policy_hash": receipt.policy_hash,
        "trace_length": receipt.trace_length,
        "zk_receipt_ref": receipt.zk_receipt_ref,
        "fhe_proof": {
            "proof_type": receipt.fhe_proof.proof_type,
            "fhe_params": receipt.fhe_proof.fhe_params,
            "encrypted_trace_hash": receipt.fhe_proof.encrypted_trace_hash,
            "policy_hash": receipt.fhe_proof.policy_hash,
            "compliance_proof": receipt.fhe_proof.compliance_proof,
            "stub": receipt.fhe_proof.stub,
        } if receipt.fhe_proof else None,
    }


def fhe_receipt_to_json(receipt: FHEReceipt, indent: Optional[int] = 2) -> str:
    """Serialize FHEReceipt to JSON string."""
    return json.dumps(fhe_receipt_to_dict(receipt), indent=indent)


def fhe_envelope_to_dict(envelope: FHEHistoryEnvelope) -> dict:
    """Serialize FHEHistoryEnvelope to JSON dict."""
    return {
        "version": envelope.version,
        "envelope_id": envelope.envelope_id,
        "created_at": envelope.created_at,
        "agent_id": envelope.agent_id,
        "policy_hash": envelope.policy_hash,
        "trace_length": envelope.trace_length,
        "ciphertext_hashes": envelope.ciphertext_hashes,
        "trace_commitment": envelope.trace_commitment,
        "fhe_params": envelope.fhe_params,
        "encrypted_metadata": envelope.encrypted_metadata,
    }


