# Demo / Smoke Test
# ---------------------------------------------------------------------------

def demo():
    """Smoke test the receipt interface."""
    print(f"PACT Receipt v{PACT_RECEIPT_VERSION}")
    print("=" * 50)

    # Build a mock receipt
    policy_commitment = PolicyCommitment(
        policy_hash="sha256:aabbccdd...",
        log_index=0,
        log_id="sha256:genesis001",
        merkle_root="sha256:merkleroot001",
        merkle_proof=[{"hash": "sha256:sibling1", "side": "right"}],
    )

    tool_call = ToolCall(
        tool_name="read_file",
        tool_input_hash=hash_tool_inputs("read_file", {"path": "/tmp/test"}),
        timestamp=datetime.now(timezone.utc).isoformat(),
        action_id=str(uuid.uuid4()),
        tool_output_hash=None,
    )

    proof = ZKProof(
        proof_type="dummy",
        image_id="pact-guest-v0.3",
        seal="DUMMY_PROOF",
        public_inputs={
            "policy_hash": policy_commitment.policy_hash,
            "merkle_root": policy_commitment.merkle_root,
            "log_index": 0,
            "tool_name_hash": "sha256:placeholder",
            "timestamp": tool_call.timestamp,
        },
    )

    receipt = create_receipt(policy_commitment, tool_call, proof)
    print(f"Receipt ID: {receipt.receipt_id}")
    print(f"Tool: {receipt.tool_call.tool_name}")
    print(f"Policy hash: {receipt.policy.policy_hash[:20]}...")
    print()

    # Serialize
    json_str = receipt_to_json(receipt, indent=2)
    print("JSON serialization:")
    print(json_str[:300])
    print()

    # SCITT envelope
    scitt = build_scitt_statement(receipt)
    print("SCITT statement payload_type:", scitt["payload_type"])
    print()

    # Verify
    result = verify_scitt_envelope(scitt)
    print("SCITT verification:", "✓ PASS" if result["valid"] else "✗ FAIL", "—", result["reason"])
    print()


if __name__ == "__main__":
    demo()
